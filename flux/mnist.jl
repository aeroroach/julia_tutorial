# ╔═╡ da882837-f795-4425-a971-6c36ef513b47
begin
	
	x_train, y_train = MLDatasets.MNIST.traindata()
	x_valid, y_valid = MLDatasets.MNIST.testdata()
	x_train = Flux.unsqueeze(x_train, 3)
	x_valid = Flux.unsqueeze(x_valid, 3)

	y_train = onehotbatch(y_train, 0:9)
	y_valid = onehotbatch(y_valid, 0:9)
	
end

# ╔═╡ 99efe128-4d5a-41c4-b873-15ef0558dbcc
typeof(x_train)

# ╔═╡ 0b134340-7c4a-4405-b18d-6ff491eae84c
size(x_train)

# ╔═╡ 350c10b6-53c8-4d33-9f8b-de04be89c6cf
md"Pack the data container for model training"

# ╔═╡ 5204a2c9-3237-4b9d-95d7-0e51326b5058
 train_data = DataLoader((data = x_train, label = y_train), batchsize=128)

# ╔═╡ c1718644-1f7e-4e25-ae6f-3af2d3c17f48
md"## ML parameters set up"

# ╔═╡ 2364c541-c416-412f-b363-843753f9bc5f
model = Chain(
    # 28x28 => 14x14
    Conv((5, 5), 1=>8, pad=2, stride=2, relu),
    # 14x14 => 7x7
    Conv((3, 3), 8=>16, pad=1, stride=2, relu),
    # 7x7 => 4x4
    Conv((3, 3), 16=>32, pad=1, stride=2, relu),
    # 4x4 => 2x2
    Conv((3, 3), 32=>32, pad=1, stride=2, relu),
    
    # Average pooling on each width x height feature map
    GlobalMeanPool(),
    flatten,
    
    Dense(32, 10),
    softmax)

# ╔═╡ f048659d-9f3d-459e-88e2-742cf716c4ee
md"Testing the initial model output"

# ╔═╡ 44239016-d80a-4b87-a233-8ebcca1d119d
begin
# Getting predictions
ŷ = model(x_train)
# Decoding predictions
ŷ = onecold(ŷ)
end

# ╔═╡ c2a3b6b2-9b5e-412b-a432-a36dd4183cd1
println("Prediction of first image: $(ŷ[1])")

# ╔═╡ 98301b27-30f7-4da3-8e01-1658e28d2b4f
accuracy(ŷ, y) = mean(onecold(ŷ) .== onecold(y))

# ╔═╡ 92091959-32df-414b-a154-1a8f6db1eecc
loss((x, y)) = Flux.crossentropy(model(x), y)

# ╔═╡ 730eba0f-3294-4523-88b9-92e5b1eceda4
begin
	lr = 0.1
	opt = Descent(lr)

end

# ╔═╡ 0b5b5ce1-8b55-4b6e-80a4-51931965c67b
ps = Flux.params(model)

# ╔═╡ 68521bd4-6f9b-4632-b7aa-fc9b91a95c68
md"## Model training"

# ╔═╡ 73786b96-4125-4524-84e8-929115595592
md"The @epochs macro can be used to execute the model iteration easly. For this example, we'll just iterate in 10 epocs"

# ╔═╡ 3f3b2997-10d9-4a64-8161-9aebf3eb2b6a
# ╠═╡ disabled = true
#=╠═╡
begin
	number_epochs = 10
	@epochs number_epochs Flux.train!(loss, ps, train_data, opt)
	accuracy(model(x_train), y_train)

end
